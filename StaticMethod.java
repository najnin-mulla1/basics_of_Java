//Calling static methods in main method
package basicprograms;

public class StaticMethod
{
	static void project()
	{
		System.out.println("This is my First Project"); 
	}
	static void automation()
	{
		System.out.println("My Automation Project");
	}
public static void main(String[] args)
{
    project();
    automation();
	System.out.println("Assignment Name is calling static method inside the main method");
 }}
	

